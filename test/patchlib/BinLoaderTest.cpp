{
    // Setup
    QTemporaryFile file;
    REQUIRE(file.open());
    file.write(kHexData.data(), kHexData.size());
    file.close();

    file.open();
    const auto contents = patchlib::BinLoader::loadFile(file);
    REQUIRE(contents.size() == kBinData.size());
    auto actual_it = contents.cbegin();
    auto expected_it = kBinData.cbegin();
    std::size_t ix = 0;
    for (; actual_it != contents.cend() && expected_it != kBinData.cend(); ++actual_it, ++expected_it, ++ix) {
        if (static_cast<unsigned char>(*actual_it) != *expected_it) {
            FAIL("Mismatch at position " << ix << " (Expected " << std::hex << static_cast<int>(*expected_it)
                                         << " actual "
                                         << static_cast<int>(*actual_it) << ")");
        }
    }
}
